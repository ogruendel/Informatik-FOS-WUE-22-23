# Erstellen der Variable 'freunde' als Listen Objekt
freunde = ['Freund 1', 'Freund 2', 'Freund 3']

# Drucken der Liste 'freunde' in die Konsole
print(freunde)

# Löschen des ersten Elements der Liste 'freunde'
freunde.pop(0)

# Drucken der geänderten Liste 'freunde' in die Konsole
print(freunde)

# Hinzufügen der Freundin Uschi in die Liste 'freunde'
freunde.append('Uschi')

# Drucken der geänderten Liste 'freunde' in die Konsole
print(freunde)
